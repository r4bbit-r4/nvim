--
-- Editor options
--

-- Use tabs instead of spaces for indenting
vim.opt.expandtab = true    -- Use spaces instead of tabs
vim.opt.tabstop = 4         -- Number of spaces per tab
vim.opt.shiftwidth = 4      -- Indent size
vim.opt.softtabstop = 4     -- Spaces per Tab while editing

-- Allow relative line numbers with absolute number at the cursor position
vim.opt.number = true         -- Show absolute number on the current line
vim.opt.relativenumber = true -- Show relative numbers elsewhere

-- Set up colors 
vim.o.termguicolors = true
vim.o.cursorline = true
